package variableTest;	

public class VariableTask {	
//	이름, 나이, 키, 취미
//	4개의 변수를 선언하여 각 값으로 초기화한 뒤
//	모두 출력한다.
	
	public static void main(String[] args) {
		String name = "임채민";	// name에 문자열 값 입력
		int age = 26;	// age에 정수 값 입력
		double tall = 165.8; 	// tall에 실수 값 입력
		String hobby = "독서";	// hobby에 문자열 입력
		
		System.out.println(name);	// name 값 출력
		System.out.println(age);	// age 값 출력
		System.out.println(tall);	// tall 값 출력
		System.out.println(hobby);	// hobby 값 출력
	}
	

	
}
